#!/usr/bin/env node
// SOURCING: @travis-gilbert/markdown-theory tokens (generateRegister, emitCss).
// Emits the computed Galley register (dark, chrome density, the package's own
// WCAG-solved ladders) into src/styles/galley-register.css so documents carry
// full document-grade type scale and rhythm inside the shell. The gy-bridge
// then re-points ground, surface, and mono at the chrome (bridged, not
// blended). Checked in; regenerate with `npm run generate:galley` after a
// markdown-theory upgrade.

import { writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { CONSOLE_AXES, generateRegister, emitCss } from '@travis-gilbert/markdown-theory/tokens';

const appRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

const register = generateRegister({ ...CONSOLE_AXES, mode: 'dark' });
const css = emitCss(register, '[data-register="intui"]');

const header = `/* GENERATED by scripts/generate-galley-register.mjs. Do not edit by hand.
   Source: @travis-gilbert/markdown-theory generateRegister(CONSOLE_AXES, mode dark),
   scoped under the Int UI register root. gy-bridge.css overrides the plane and
   mono tokens after this file so Galley grounds bridge to the chrome. */

`;

writeFileSync(path.join(appRoot, 'src/styles/galley-register.css'), header + css + '\n');
console.log('galley-register.css generated.');
